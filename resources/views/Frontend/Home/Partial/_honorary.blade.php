                <h2>প্রাপ্তির সম্মাননা</h2>
                <p style="text-align: justify;">প্রতি বছর বই পড়া প্রতিযোগিতায় অংশগ্রহণের জন্য মাধ্যমিক বিদ্যালয় পর্যায় আবেদন করা হয়। নির্বাচিত বিদ্যালয়ের প্রধান শিক্ষক শ্রেণীভিত্তিক বই থেকে মেধা মূল্যায়নের জন্য সাধারণ পরীক্ষা গ্রহণ করবেন এবং প্রতিটি শ্রেণী থেকে তিনজনকে বিজয়ী ঘোষণা করবেন। বিজয়ীদের স্মরণিয় করে রাখার জন্য প্রণয়ী পাঠাগার কর্তৃক সম্মানিদের সম্মাননা ফুল/চাদর/মুকুট দিয়ে বরণ করে নিবেন।<br>

                দিনটি স্মরণিয় এবং মহিমান্বিত করে স্মৃতির পাতায় গেথে রাখার জন্য কিছু আনুষ্ঠানিকতা পালন করা হয়। বিজয়ীদের তালিকা চূড়ান্ত পর্যায় প্রস্তুত হলে প্রতি বছর ২৬ মার্চ সাংস্কৃতিক অনুষ্ঠানের মধ্য দিয়ে দিনটি পালন করা হয়। এ অনুষ্ঠানের প্রধান অতিথি হিসেবে গণ্য হবেন- গ্রামের গুণীজন, ইউপি চেয়ারম্যান, উপজেলা চেয়ারম্যান, সংসদ সদস্য, সাহিত্যানুরাগী এবং গণ্যমান্য ব্যক্তিবর্গ।<br>

                মানুষ জীবনের প্রতিটি স্তরে এসেও উপহার পেতে পছন্দ করেন। আর তা যদি হয় বই; কোনো কথা নেই, চোখ বন্ধ করে, একদমে শুষে নেই নতুন বইয়ের গন্ধ। প্রধান অতিথি ছাত্র/ছাত্রীদের মধ্যে বই উপহার দিয়ে আগামীর জন্য নতুন বই বিতরণের যাত্রা শুরু করবেন। বই পড়ায় যে আনন্দ আছে সে বিষয়ে প্রবন্ধ, গল্প, কবিতা উপস্থাপনের মধ্যে দিয়ে মানুষকে বই পড়তে উদ্বুদ্ধ করবেন।</p>
